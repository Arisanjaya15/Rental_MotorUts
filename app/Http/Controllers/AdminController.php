<?php

namespace App\Http\Controllers;

use Illuminate\Http\Request;

use App\Motor;
class AdminController extends Controller
{
    /**
     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index()
    {
        $title='Motor';
        $motor=Motor::paginate(3);
        return view('admin.dashboard',compact('title','motor'));
    }

    /**
     * Show the form for creating a new resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function create()
    {
        $title='Input Motor';
        return view('admin.inputmotor',compact('title'));
    }

    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $messages = [
            'required'=>'Kolom :attribute harus lengkap',
            'date'    =>'Kolom :attribute harus Tanggal',
            'numeric' =>'Kolom :attribute harus Angka',
        ];
        $validasi = $request->validate([ 
            'merk_motor'=>'required',
            'plat_motor'=>'required',
            'warna_motor'=>'required',
            'tahun_motor'=>'required'
        ],$messages);

        Motor::create($validasi);
        return redirect('rental')->with('succes','data berhasil di update');
    }

    /**
     * Display the specified resource.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function show($id)
    {
        //
    }

    /**
     * Show the form for editing the specified resource.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function edit($id)
    {
        $title='Input Motor';
        $motor=Motor::find($id);
        return view('admin.inputmotor',compact('title','motor'));
    }

    /**
     * Update the specified resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function update(Request $request, $id)
    {
        $messages = [
            'required'=>'Kolom :attribute harus lengkap',
            'date'    =>'Kolom :attribute harus Tanggal',
            'numeric' =>'Kolom :attribute harus Angka',
        ];
        $validasi = $request->validate([ 
            'merk_motor'=>'required',
            'plat_motor'=>'required',
            'warna_motor'=>'required',
            'tahun_motor'=>'required'
        ],$messages);

        Motor::whereid_motor($id)->update($validasi);
        return redirect('rental')->with('succes','data berhasil di update');
    }

    /**
     * Remove the specified resource from storage.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function destroy($id)
    {
        Motor::whereid_motor($id)->delete();
        return redirect('rental')->with('succes','data berhasil di update');
    }
}
